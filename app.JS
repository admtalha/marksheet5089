var a = parseFloat(prompt("Write Your Percentage To Know Your Grade" , "0"));
if(a <=49.99){
document.write("FAIL !");
}
else if (a <= 59.99) {
    document.write ("C");
}

else if (a <= 69.99) {
    document.write ("B");
} 
 else if (a <= 79.99) {
    document.write("A");

} else if (a <= 89.99) {
    document.write ("A-1");
}  
 else if (a <= 99.99) {
    document.write ("A+");
} 
